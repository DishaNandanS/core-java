class Makeup {
private String brand;
private String type;
private double cost;

public void setBrand(String brand) {
this.brand = brand;
}

public String getBrand() {
return brand;
}

public void setType(String type) {
this.type = type;
}

public String getType() {
return type;
}

public void setCost(double cost) {
this.cost = cost;
}

public double getCost() {
return cost;
}
}
